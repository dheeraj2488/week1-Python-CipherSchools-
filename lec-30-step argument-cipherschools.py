#slicing /selecting sub sequencees
#we done step argument by 
# [start argument:stop argument:step argument]
name="Dheeraj"
print(name[0:5:2]) #it wil take 2 step from starting argument
print(name[::3])
print(name[5::-1]) #reversing the string from 5 index
# for reversing the string
print(name[::-1])
print(name[-1::-1])