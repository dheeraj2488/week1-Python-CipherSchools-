# syntax used for slicing [starting argument:stopping argument] we get stop argument by -1
#index starts from 0 to n
name="python"
print(name[0:2]) #if we want first two elemnts or we can say that the stopping argument index will not be included 
print(name[-3:4])
# if we want full String 
print(name[:])

print(name[1:]) #starts from second element

print(name[:4]) #4index will not be included
